                THE WHALE È UNA STORIA VERA? ECCO A CHI È ISPIRATO IL FILM
               </div>
               <br>
               <div>
                The Whale è una storia vera? La domanda è legittima, perché nonostante la natura chiaramente artefatta della narrazione – che condensa molteplici snodi in pochissimo tempo e ancor meno spazio – le vicende raccontate sono in fin dei conti verosimili. D’altronde guardando il disastro che è diventata la vita di Charlie viene facile pensare a quelle «vite al limite» che sfilano davanti al dott. Nowzaradan in un celebre reality sul canale del digitale terrestre Real Time. The Whale è un film di finzione, eppure l’elemento biografico è nettamente presente. L’autore del dramma teatrale nonché sceneggiatore della pellicola Samuel D. Hunter, infatti, ha molti punti in comune col protagonista: ha vissuto a Moscow in Idaho, ha insegnato saggistica alla Rutgers University, è dichiaratamente omosessuale e in passato ha sofferto di gravi disturbi alimentari. Questo fa di The Whale un film solo parzialmente autobiografico.
